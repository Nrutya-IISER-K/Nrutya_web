            function extractAllMediaRecursively(node, currentFolderName = "Gallery Root") {
                let items = [];
                if (!node) return items;
                
                let activeFolderContext = currentFolderName;
                if (node.name) {
                    activeFolderContext = node.name;
                }

                if (node.media && Array.isArray(node.media)) {
                    node.media.forEach(m => {
                        items.push({
                            ...m,
                            derivedFolderContext: activeFolderContext
                        });
                    });
                }
                if (node.folders && Array.isArray(node.folders)) {
                    node.folders.forEach(sub => {
                        items = items.concat(extractAllMediaRecursively(sub, activeFolderContext));
                    });
                }
                return items;
            }

            function generateAllCategoryFlatGrid() {
                let ultimateMediaPool = [];
                Object.keys(GALLERY_DATA).forEach(catKey => {
                    ultimateMediaPool = ultimateMediaPool.concat(extractAllMediaRecursively(GALLERY_DATA[catKey]));
                });
                return ultimateMediaPool;
            }

            function drawActiveDirectoryView() {
                mainGrid.innerHTML = ""; 
                const activeCategory = filterButtons[currentCategoryIndex].getAttribute('data-category');

                if (activeCategory === 'all') {
                    backBtn.style.display = 'none';
                    subTitle.textContent = "Movements. Moments. Memories.";
                    
                    const totalMediaList = generateAllCategoryFlatGrid();
                    if(totalMediaList.length === 0) {
                        mainGrid.innerHTML = `<div style="grid-column: 1/-1; text-align: center; color: var(--text-secondary); padding: 40px 0;">No media available yet!</div>`;
                        return;
                    }
                    totalMediaList.forEach(media => appendMediaToDOM(media, media.derivedFolderContext));
                    return;
                }

                let currentNodeTarget = GALLERY_DATA[activeCategory];
                let trackingContextName = filterButtons[currentCategoryIndex].textContent; 
                
                navigationDirectoryHistory.forEach(folderName => {
                    if (currentNodeTarget && currentNodeTarget.folders) {
                        currentNodeTarget = currentNodeTarget.folders.find(f => f.name === folderName);
                        trackingContextName = folderName; 
                    }
                });

                if (navigationDirectoryHistory.length > 0) {
                    backBtn.style.display = 'flex';
                    subTitle.textContent = "Album Path: " + ["Gallery", filterButtons[currentCategoryIndex].textContent].concat(navigationDirectoryHistory).join(' / ');
                } else {
                    backBtn.style.display = 'none';
                    subTitle.textContent = `Albums / ${filterButtons[currentCategoryIndex].textContent}`;
                }

                if (!currentNodeTarget) return;

                let renderedItemsCount = 0;

                if (currentNodeTarget.folders && Array.isArray(currentNodeTarget.folders)) {
                    currentNodeTarget.folders.forEach(subFolder => {
                        renderedItemsCount++;
                        const card = document.createElement('div');
                        card.className = "folder-card";
                        card.innerHTML = `
                            <i class="fa-solid fa-folder-closed folder-icon"></i>
                            <div class="folder-title-text">${subFolder.name}</div>
                            <div class="folder-count">Open Folder</div>
                        `;
                        card.addEventListener('click', () => {
    navigationDirectoryHistory.push(subFolder.name);
    
    // NEW: Save the new folder path to the browser's history
    history.pushState(
        { directoryHistory: [...navigationDirectoryHistory], categoryIndex: currentCategoryIndex }, 
        ''
    );
    
    drawActiveDirectoryView();
});
                        mainGrid.appendChild(card);
                    });
                }

                if (currentNodeTarget.media && Array.isArray(currentNodeTarget.media)) {
                    currentNodeTarget.media.forEach(media => {
                        renderedItemsCount++;
                        appendMediaToDOM(media, trackingContextName);
                    });
                }

                if (renderedItemsCount === 0) {
                    mainGrid.innerHTML = `<div style="grid-column: 1/-1; text-align: center; color: var(--text-secondary); padding: 40px 0;">This directory level is empty.</div>`;
                }
            }

            function appendMediaToDOM(media, contextLabel) {
                const item = document.createElement('div');
                item.className = `gallery-item ${media.layout || ''}`;
                
                const isYouTube = media.src.includes('youtube.com') || media.src.includes('youtu.be');

                if (media.type === 'video') {
                    // Use a disabled iframe for the grid preview if it's YouTube, otherwise use the video tag
                    const previewContent = isYouTube 
                        ? `<iframe src="${media.src}?autoplay=0&controls=0&mute=1" style="width:100%; height:100%; pointer-events:none;" frameborder="0"></iframe>`
                        : `<video src="${media.src}" muted loop autoplay playsinline></video>`;

                    item.innerHTML = `
                        <div class="video-badge"><i class="fa-solid fa-play"></i></div>
                        ${previewContent}
                        <div class="item-overlay">
                            <div class="item-title">${media.title}</div>
                            <div class="item-category">${contextLabel}</div>
                        </div>
                    `;
                } else {
                    item.innerHTML = `
                        <img src="${media.src}" alt="${media.title}">
                        <div class="item-overlay">
                            <div class="item-title">${media.title}</div>
                            <div class="item-category">${contextLabel}</div>
                        </div>
                    `;
                }

                item.addEventListener('click', () => {
                    // Reset all viewers
                    lightboxImg.classList.remove('visible');
                    lightboxVideo.classList.remove('visible');
                    lightboxIframe.classList.remove('visible');
                    
                    lightboxVideo.pause();
                    lightboxVideo.src = "";
                    lightboxIframe.src = "";

                    // Route the media to the correct viewer
                    if (media.type === 'video') {
                        if (isYouTube) {
                            lightboxIframe.src = media.src;
                            lightboxIframe.classList.add('visible');
                        } else {
                            lightboxVideo.src = media.src;
                            lightboxVideo.classList.add('visible');
                            lightboxVideo.play();
                        }
                    } else {
                        lightboxImg.src = media.src;
                        lightboxImg.classList.add('visible');
                    }

                    lightboxCaption.textContent = media.title;
                    lightbox.classList.add('active');
                    document.body.style.overflow = 'hidden';
                });

                mainGrid.appendChild(item);
            }
            

